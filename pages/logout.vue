import { mapMutations } from 'vuex'
export default {
  middleware: ['authenticated'],
  head: {
    title: 'Logout'
  },
  methods: {
    ...mapMutations('auth', {
        logout: 'logout'
    }),
  },
  mounted() {
    this.logout()
    this.$router.push('/login')
  }
}
</script>
