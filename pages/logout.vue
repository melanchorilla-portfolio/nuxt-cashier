<template>
  <v-row>
    <v-col cols="10" offset="1" md="4" offset-md="4">
      <v-card class="mb-2">
        <v-card-text>
          <h2>
            Logoout <v-icon class="ml-1 spin-loading">mdi-loading</v-icon>
          </h2>
        </v-card-text>
      </v-card>
    </v-col>
  </v-row>
</template>

